                    break;
                case "1":
                    G = "Código distorsionado";
                    break;
                default:
                    G = "N/A";
                    break;

            }

            L = arrayString[0][2].ToString();
            switch (L)
            {
                case "0":
                    L = "Código Mode-3/A derivado de la respuesta XPDR";
                    break;
                case "1":
                    L = "Código Mode-3/A no se ha extraído en el último escaneo";
                    break;
                default:
                    L = "N/A";
                    break;

            }

