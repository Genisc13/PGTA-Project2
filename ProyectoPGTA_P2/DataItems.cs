                    case "00":
                        FOE_FRI = "No mode 4 interrogation";
                        break;
                    case "01":
                        FOE_FRI = "Friendly target";
                        break;
                    case "10":
                        FOE_FRI = "Unknown target";
                        break;
                    case "11":
                        FOE_FRI = "No reply";
                        break;
                    default:
                        FOE_FRI = "N/A";
                        break;
                }

                FX2 = b[7].ToString();
                if (FX1 == "1")
                {
